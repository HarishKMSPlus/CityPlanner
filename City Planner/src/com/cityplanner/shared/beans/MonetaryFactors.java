package com.cityplanner.shared.beans;

public class MonetaryFactors {

	double totalBudgetInRs;
	double reservedBudgetInRs;
	double rupeesPerSqMeter;

	public double getTotalBudgetInRs() {
		return totalBudgetInRs;
	}

	public void setTotalBudgetInRs(double totalBudgetInRs) {
		this.totalBudgetInRs = totalBudgetInRs;
	}

	public double getReservedBudgetInRs() {
		return reservedBudgetInRs;
	}

	public void setReservedBudgetInRs(double reservedBudgetInRs) {
		this.reservedBudgetInRs = reservedBudgetInRs;
	}

	public double getRupeesPerSqMeter() {
		return rupeesPerSqMeter;
	}

	public void setRupeesPerSqMeter(double rupeesPerSqMeter) {
		this.rupeesPerSqMeter = rupeesPerSqMeter;
	}

}
